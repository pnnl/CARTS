/** @brief Compute block
 ** @param self HOG object.
 ** @param image image to process.
 ** @param x block colum (Cell 0x0 of the block)
 ** @param y block row (Cell 0x0 of the block)
 *
 ** @param features HOG features (output).
 **
 **/
void vl_bsc_compute_block(VlHog *self, vl_float const *image, vl_index by,
                          vl_index bx) {
  // printf("called for block %d %d\n", by, bx);

  vl_index cx, cy, px, py;
  vl_uindex k;

  /* 2x2 cells in a block */
  for (cy = by; cy < by + 3; cy++) {
    for (cx = bx; cx < bx + 3; cx++) {
      if (cy < HOG_HEIGHT && cx < HOG_WIDHT &&
          self->cell_GradComputed[cx + cy * HOG_WIDHT] ==
              0) { // Cell not computed

        /* 8x8 pixels in a cell */
        for (py = cy * CELL_SIZE; py < cy * CELL_SIZE + CELL_SIZE; py++) {
          for (px = cx * CELL_SIZE; px < cx * CELL_SIZE + CELL_SIZE; px++) {

            /* Check pixels in the image borders */
            if (py > 0 && px > 0 && py < HEIGHT_FullHD - 1 &&
                px < WIDTH_FullHD - 1) {
              /* Compute gradient pixel (py,px) and accumulate
              it to the 9-bin histogram of the cell */
              vl_float const *pixel =
                  image + py * WIDTH_FullHD * NUM_CHANNELS + px * NUM_CHANNELS;
              vl_bsc_compute_gradient(self, pixel, px, py);
            }
          }
        }
        self->cell_GradComputed[cx + cy * HOG_WIDHT] = 1;
      } /* End of 8x8 pixels in the CELL  */
    }
  } /* End of 2x2 cells in the BLOCK */

  for (cy = by; cy < by + 2; cy++) {
    for (cx = bx; cx < bx + 2; cx++) {
      if (self->cell_NormComputed[cx + cy * HOG_WIDHT] ==
          0) { // Cell norm not computed

        /*
        Compute the squared L2 norm of the unoriented version of each HOG
        cell histogram. The unoriented version is obtained by folding
        the 2*numOrientations compotnent into numOrientations only.
        */
        hogNorm[cy][cx] = 0.0;
        for (k = 0; k < NUM_ORIENTATIONS; ++k) {
          vl_float h1 = hog[cy][cx][k];
          vl_float h2 = hog[cy][cx][k + NUM_ORIENTATIONS];
          vl_float h = h1 + h2;
          hogNorm[cy][cx] += h * h;
        }
        self->cell_NormComputed[cx + cy * HOG_WIDHT] = 1;
      }
    }
  }

  /* End of 2x2 cells in the BLOCK */
  vl_bsc_block_normalization(self, by, bx);

  // If window complete then call svm
  if ((by >= WINHEIGHT - 1) && (bx >= WINWIDTH - 1)) {
    vl_bsc_predictWindow(by, bx);
  }
}
