/** @brief Check if window contains person
 ** @param bx block colum (Cell 0x0 of the block)
 ** @param by block row (Cell 0x0 of the block)
 **        (by,bx) -- Last block of the window
 *
 ** @param dec_value >0 if person detected, <=0 otherwise
 **
 **/
vl_float vl_bsc_predictWindow(vl_index by, vl_index bx) {
  vl_index wy, wx, h1, h2;
  vl_float dec_value = 0.0;
  int idx = 0;

  for (wy = by - WINHEIGHT + 1; wy <= by; wy++) {
    for (wx = bx - WINWIDTH + 1; wx <= bx; wx++) {
      for (h1 = 0; h1 < 4; h1++) {
        for (h2 = 0; h2 < NUM_ORIENTATIONS; h2++) {
          dec_value += svm[idx] * features[wy][wx][h1][h2];
          idx++;
        }
      }
    }
  }

  /* nWindow = window.y * NWINDOWS.Y + window.x;
     (window.y,window.x) given by block (0,0) of the window */
  int nWindow =
      (by - WINHEIGHT + 1) * (HOG_WIDHT - WINWIDTH) + (bx - WINWIDTH + 1);

  if (dec_value > 0) {
    windows[nWindow].py = (by - WINHEIGHT + 1) * CELL_SIZE;
    windows[nWindow].px = (bx - WINWIDTH + 1) * CELL_SIZE;
    windows[nWindow].weight = dec_value;
  } else {
    windows[nWindow].weight = 0.0;
  }
  // printf("Window %d (by=%d,bx=%d)=%f\n", nWindow, by, bx, dec_value);

  return dec_value;
}

