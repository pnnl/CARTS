/** @brief Delete a HOG object
 ** @param self HOG object to delete.
 **/

void vl_hog_delete(VlHog *self) {
  if (self->orientationX) {
    free(self->orientationX);
    self->orientationX = NULL;
  }

  if (self->orientationY) {
    free(self->orientationY);
    self->orientationY = NULL;
  }
  if (self->cell_GradComputed) {
    free(self->cell_GradComputed);
    self->cell_GradComputed = NULL;
  }

  if (self->cell_NormComputed) {
    free(self->cell_NormComputed);
    self->cell_NormComputed = NULL;
  }
  free(self);
}

