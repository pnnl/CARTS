/** @brief Compute gradient
 ** @param self HOG object.
 ** @param x pixel colum.
 ** @param y pixel row.
 ** @param pixel pixel to process.
 **
 **
 **/
void vl_bsc_compute_gradient(VlHog *self, vl_float const *pixel, vl_index x,
                             vl_index y) {
  vl_uindex k;
  vl_size channelStride = WIDTH_FullHD * HEIGHT_FullHD;

  vl_float gradx = 0;
  vl_float grady = 0;
  vl_float gradNorm;
  vl_float orientationWeights[2] = {-1, -1};
  vl_index orientationBins[2] = {-1, -1};
  vl_index orientation = 0;
  vl_float hx, hy, wx1, wx2, wy1, wy2;
  vl_index binx, biny, o;

  /*
  Compute the gradient at (x,y). The image channel with
  the maximum gradient at each location is selected.
  */
  {
    vl_float const *iter = pixel;
    vl_float gradNorm2 = 0;
    for (k = 0; k < NUM_CHANNELS; ++k) {
      vl_float gradx_ = *(iter + NUM_CHANNELS) - *(iter - NUM_CHANNELS);
      vl_float grady_ = *(iter + WIDTH_FullHD * NUM_CHANNELS) -
                        *(iter - WIDTH_FullHD * NUM_CHANNELS);
      vl_float gradNorm2_ = gradx_ * gradx_ + grady_ * grady_;
      if (gradNorm2_ > gradNorm2) {
        gradx = gradx_;
        grady = grady_;
        gradNorm2 = gradNorm2_;
      }
      iter++;
    }
    gradNorm = sqrtf(gradNorm2);
  }
  /*
  Map the gradient to the closest and second closets orientation bins.
  There are numOrientations orientation in the interval [0,pi).
  The next numOriantations are the symmetric ones, for a total
  of 2*numOrientation directed orientations.
  */
  for (k = 0; k < NUM_ORIENTATIONS; ++k) {
    vl_float orientationScore_ =
        gradx * self->orientationX[k] + grady * self->orientationY[k];
    vl_index orientationBin_ = k;
    if (orientationScore_ < 0) {
      orientationScore_ = -orientationScore_;
      orientationBin_ += NUM_ORIENTATIONS;
    }
    if (orientationScore_ > orientationWeights[0]) {
      orientationBins[1] = orientationBins[0];
      orientationWeights[1] = orientationWeights[0];
      orientationBins[0] = orientationBin_;
      ;
      orientationWeights[0] = orientationScore_;
    } else if (orientationScore_ > orientationWeights[1]) {
      orientationBins[1] = orientationBin_;
      orientationWeights[1] = orientationScore_;
    }
  }

  orientationWeights[0] = 1;
  orientationBins[1] = -1;

  for (o = 0; o < 2; ++o) {
    vl_float ow;
    /*
    Accumulate the gradient. hx is the distance of the
    pixel x to the cell center at its left, in units of cellSize.
    With this parametrixation, a pixel on the cell center
    has hx = 0, which gradually increases to 1 moving to the next
    center.
    */

    orientation = orientationBins[o];
    if (orientation < 0)
      continue;

    /*  (x - (w-1)/2) / w = (x + 0.5)/w - 0.5 */
    hx = (x + 0.5) / CELL_SIZE - 0.5;
    hy = (y + 0.5) / CELL_SIZE - 0.5;
    binx = vl_floor_d(hx);
    biny = vl_floor_d(hy);
    wx2 = hx - binx;
    wy2 = hy - biny;
    wx1 = 1.0 - wx2;
    wy1 = 1.0 - wy2;

    ow = orientationWeights[o];

    // printf("%d %d - %d %d %d - %f %f - %f %f %f %f - %d - %f %f\n
    // ",x,y,binx,biny,orientation,hx,hy,wx1,wx2,wy1,wy2,o,ow,gradNorm);

    if (binx >= 0 && biny >= 0) {
      vl_float bin = gradNorm * ow * wx1 * wy1;
      vl_float *p =
          &hog[0][0][0] + ((biny * HOG_WIDHT * 2 * NUM_ORIENTATIONS) +
                           (binx * 2 * NUM_ORIENTATIONS) + orientation);
#if (defined OMP) || (defined OMPSS)
#pragma omp atomic
#endif
      *p += bin;
    }
    if (binx < HOG_WIDHT - 1 && biny >= 0) {
      vl_float bin = gradNorm * ow * wx2 * wy1;
      vl_float *p =
          &hog[0][0][0] + ((biny * HOG_WIDHT * 2 * NUM_ORIENTATIONS) +
                           ((binx + 1) * 2 * NUM_ORIENTATIONS) + orientation);
#if (defined OMP) || (defined OMPSS)
#pragma omp atomic
#endif
      *p += bin;
    }
    if (binx < HOG_WIDHT - 1 && biny < HOG_HEIGHT - 1) {
      vl_float bin = gradNorm * ow * wx2 * wy2;
      vl_float *p =
          &hog[0][0][0] + (((biny + 1) * HOG_WIDHT * 2 * NUM_ORIENTATIONS) +
                           ((binx + 1) * 2 * NUM_ORIENTATIONS) + orientation);
#if (defined OMP) || (defined OMPSS)
#pragma omp atomic
#endif
      *p += bin;
    }
    if (binx >= 0 && biny < HOG_HEIGHT - 1) {
      vl_float bin = gradNorm * ow * wx1 * wy2;
      vl_float *p =
          &hog[0][0][0] + (((biny + 1) * HOG_WIDHT * 2 * NUM_ORIENTATIONS) +
                           (binx * 2 * NUM_ORIENTATIONS) + orientation);
#if (defined OMP) || (defined OMPSS)
#pragma omp atomic
#endif
      *p += bin;
    }
  }
}

